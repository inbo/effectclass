test_that("change_labels()", {
  expect_identical(change_labels(log(0.5)), "-50%")
  expect_identical(change_labels(log(0.75)), "-25%")
  expect_identical(change_labels(log(1.211)), "+21%")
  expect_identical(change_labels(log(1.194)), "+19%")
  expect_identical(change_labels(log(1.104)), "+10%")
  expect_identical(change_labels(log(1.094)), "+9%")
  expect_identical(change_labels(log(1.014)), "+1.4%")
  expect_identical(change_labels(log(1.0106)), "+1.1%")
  expect_identical(change_labels(log(1.0096)), "+1.0%")
  expect_identical(change_labels(log(1.0094)), "+0.9%")
  expect_identical(change_labels(log(1.0014)), "+0.14%")
  expect_identical(change_labels(log(1.00096)), "+0.10%")
  expect_identical(change_labels(log(1.00094)), "+0.09%")
  expect_identical(change_labels(log(1.00014)), "+0.014%")
})
